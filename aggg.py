import numpy as np
a = np.array([1, 2, 3, 4, 5])
print(a)
sum_a = np.sum(a)  # Numpy function
print("sum",sum_a)
median_a = np.median(a)  # Numpy function
print("median",median_a)
mean_a = np.mean(a)  # Numpy function
print("mean",mean_a)
minimum_a = np.min(a)  # Numpy function
print("minimum",minimum_a)
maximum_a = np.max(a)  # Numpy function
print("maximum",maximum_a)
imin_a = np.argmin(a)  # Numpy function
print("imin",imin_a)
imax_a = np.argmax(a)  # Numpy function
print("imax",imax_a)
standard_deviation_a = np.std(a)  # Numpy function
print("standard deviation",standard_deviation_a)
variance_a = np.var(a)  # Numpy function
print("variance",variance_a)
